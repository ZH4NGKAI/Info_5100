    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:

        int selectedrow = abVitalSignsTable.getSelectedRow();
        if(selectedrow >= 0) {
            VitalSigns vs = (VitalSigns)abVitalSignsTable.getValueAt(selectedrow, 0);
            bloodPresTextField.setText(String.valueOf(vs.getBloodPressure()));
            tempratureTextField.setText(String.valueOf(vs.getTemperature()));
            pulseTextField.setText(String.valueOf(vs.getPulse()));
            dateTextField.setText(vs.getDate());
            abVitalSignsTable.setEnabled(false);
            bloodPresTextField.setEnabled(true);
            tempratureTextField.setEnabled(true);
            pulseTextField.setEnabled(true);
            dateTextField.setEnabled(true);
            saveBtn.setEnabled(true);
            deleteBtn.setEnabled(false);
            viewDetailBtn.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(null, "Please select any row!");
        }

    }//GEN-LAST:event_updateBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:

        int selectedrow = abVitalSignsTable.getSelectedRow();
        if(selectedrow >= 0) {
            VitalSigns v = (VitalSigns)abVitalSignsTable.getValueAt(selectedrow, 0);
            double temperature = Double.parseDouble(tempratureTextField.getText());
            double bp = Double.parseDouble(bloodPresTextField.getText());
            int pulse = Integer.parseInt(pulseTextField.getText());
            String date = dateTextField.getText();
            v.setBloodPressure(bp);
            v.setTemperature(temperature);
            v.setPulse(pulse);
            v.setDate(date);
            populateTable();
            JOptionPane.showMessageDialog(null, "Vital Sign Updated Successfully");
            tempratureTextField.setText("");
            bloodPresTextField.setText("");
            pulseTextField.setText("");
            dateTextField.setText("");
            bloodPresTextField.setEnabled(false);
            tempratureTextField.setEnabled(false);
            pulseTextField.setEnabled(false);
            dateTextField.setEnabled(false);
            abVitalSignsTable.setEnabled(true);
            saveBtn.setEnabled(false);
            deleteBtn.setEnabled(true);
            viewDetailBtn.setEnabled(true);
        }else
        JOptionPane.showMessageDialog(null, "Please select any row!");
    }//GEN-LAST:event_saveBtnActionPerformed

