    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel)abVitalSignsTable.getModel();
        dtm.setRowCount(0);
        
        for (VitalSigns vs: vsh.getVitalSignHistory()) {
            if (vs.getBloodPressure()>maxBloodPressure || vs.getBloodPressure()<minBloodPressure) {   
                Object row[] = new Object[2];
                row[0] = vs;
                row[1] = vs.getBloodPressure();
                dtm.addRow(row);
            }
        }
    }
    
