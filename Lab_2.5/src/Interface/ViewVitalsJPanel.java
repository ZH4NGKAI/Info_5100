    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
        // TODO add your handling code here:
        BloodPresTextField.setEnabled(false);
        TempratureTextField.setEnabled(false);
        PulseTextField.setEnabled(false);
        DateTextField.setEnabled(false);
        int selectedrow = VitalSignsTable.getSelectedRow();
        if(selectedrow >= 0) {
            VitalSigns vs = (VitalSigns)VitalSignsTable.getValueAt(selectedrow, 0);
            BloodPresTextField.setText(String.valueOf(vs.getBloodPressure()));
            TempratureTextField.setText(String.valueOf(vs.getTemperature()));
            PulseTextField.setText(String.valueOf(vs.getPulse()));
            DateTextField.setText(vs.getDate());
            int reply = JOptionPane.showConfirmDialog(null, "Are you sure to delete?", null,JOptionPane.YES_NO_OPTION);
            if(reply == 0) {
                vsh.deleteVitals(vs);
                BloodPresTextField.setText("");
                TempratureTextField.setText("");
                PulseTextField.setText("");
                DateTextField.setText("");
            }else 
                return;
            populateTable();
            JOptionPane.showMessageDialog(null, "Vital sign has been deleted!");
            
        } else {
            JOptionPane.showMessageDialog(null, "Please select any row!");
        }
    }//GEN-LAST:event_DeleteBtnActionPerformed

