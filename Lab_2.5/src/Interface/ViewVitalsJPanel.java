    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        
        int selectedrow = VitalSignsTable.getSelectedRow();
        if(selectedrow >= 0) {
            VitalSigns v = (VitalSigns)VitalSignsTable.getValueAt(selectedrow, 0);
            double temperature = Double.parseDouble(TempratureTextField.getText());
            double bp = Double.parseDouble(BloodPresTextField.getText());
            int pulse = Integer.parseInt(PulseTextField.getText());
            String date = DateTextField.getText();
            v.setBloodPressure(bp);
            v.setTemperature(temperature);
            v.setPulse(pulse);
            v.setDate(date);
            JOptionPane.showMessageDialog(null, "Vital Sign Updated Successfully");
            populateTable();
            TempratureTextField.setText("");
            BloodPresTextField.setText("");
            PulseTextField.setText("");
            DateTextField.setText("");
            BloodPresTextField.setEnabled(false);
            TempratureTextField.setEnabled(false);
            PulseTextField.setEnabled(false);
            DateTextField.setEnabled(false);
            VitalSignsTable.setEnabled(true);
            saveBtn.setEnabled(false);
            DeleteBtn.setEnabled(true);
            ViewDetailBtn.setEnabled(true);
        }else 
            JOptionPane.showMessageDialog(null, "Please select any row!");
    }//GEN-LAST:event_saveBtnActionPerformed


