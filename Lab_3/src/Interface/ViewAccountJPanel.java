    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        Component[] comps = this.userProcessContainer.getComponents();
        for (Component comp: comps) {
            if (comp instanceof ManageAccountJPanel) {
                ManageAccountJPanel panel = (ManageAccountJPanel) comp;
                panel.populateTable();
            }
        }
        layout.previous(userProcessContainer);
        
        
        
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String routingNum = txtRoutingNum.getText();
        String accountNum = txtAccountNum.getText();
        
        boolean duplication = accountDirectory.checkDuplication(accountNum, routingNum);
        if (!duplication){
            account.setAccountNumber(accountNum);
            account.setRoutingNumber(routingNum);
            account.setBankName(txtBankName.getText());
            btnSave.setEnabled(false);
            btnUpdate.setEnabled(true);
            JOptionPane.showMessageDialog(null, "Account updated successfully!");
        }
        else {
            JOptionPane.showMessageDialog(null, "Failure to update account: existed account number or routing number!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

