    private void btnLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogInActionPerformed
        // TODO add your handling code here:
        String password = String.valueOf(txtTravelOfficePwd.getPassword());
        TravelOffice to = (TravelOffice) comboTravelOffice.getSelectedItem();
        if(to!=null) {
            if(password.isEmpty()){
                JOptionPane.showMessageDialog(null, "password is empty!");
                return;
            }   
            if (to.verify(password)) {
                txtTravelOfficePwd.setText("");
                comboTravelOffice.setSelectedIndex(-1);
                grantAccess(to);
            }
            else {
                JOptionPane.showMessageDialog(null, "Please enter a valid password!");
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Please choose Travel Office!");
            return;
        }
            
    }//GEN-LAST:event_btnLogInActionPerformed

