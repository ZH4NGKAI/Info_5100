    private void displayCar(Car car) {
        
        String carMake = car.getMake();
        makeTextField.setText(carMake);
        
        String carModel = car.getModel();
        modelTextField.setText(carModel);
        
        String carPrice = car.getPrice();
        priceTextField.setText(carPrice);
        
        String carBodyStyle = car.getBodyStyle();
        bodyStyleTextField.setText(carBodyStyle);
        
        String carDoorNum = car.getDoorNum();
        doorNumTextField.setText(carDoorNum);
        
        String carExtColor = car.getExtColor();
        extColorTextField.setText(carExtColor);
        
        String carIntColor = car.getIntColor();
        intColorTextField.setText(carIntColor);
        
        String carCityMpg = car.getCityMpg();
        cityMpgTextField.setText(carCityMpg);
        
        String carHwyMpg = car.getHwyMpg();
        hwyMpgTextField.setText(carHwyMpg);
        
        String carFuelType = car.getFuelType();
        fuelTypeTextField.setText(carFuelType);
        
        String carEngine = car.getEngine();
        engineTextField.setText(carEngine);
        
        String carCylinders = car.getCylindersNum();
        cylindersTextField.setText(carCylinders);
        
        String carDimensions = car.getDimensions();
        dimensionsTextField.setText(carDimensions);
        
        String carDrivetrain = car.getDrivetrain();
        drivetrainTextField.setText(carDrivetrain);
        
        String carTransmission = car.getTransmission();
        transmissionTextField.setText(carTransmission);
        
        String carVin = car.getVin();
        vinTextField.setText(carVin);
        
        String carMileage = car.getMileage();
        mileageTextField.setText(carMileage);
        
        String carSpeakerNum = car.getSpeakerNum();
        speakerNumTextField.setText(carSpeakerNum);
        
        String carSeatNum = car.getSeatNum();
        seatNumTextField.setText(carSeatNum);
        
        String carSeatType = car.getSeatType();
        seatTypeTextField.setText(carSeatType);
        
        String imageName = car.getImageName();
        viewImageBtn.setName(imageName);
    }
