        // TODO add your handling code here:
        String imageName = viewImageBtn.getName();
        if(imageName == null) {
            JOptionPane.showMessageDialog(null, 
                    "Can't Find Picture of Your Car!",
                    "Warning",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        ImageIcon IC = new ImageIcon("image/"+imageName);
//        int iconWidth = IC.getIconWidth();
//        int iconHeight = IC.getIconHeight();
        IC.setImage(IC.getImage());
//      IC.setImage(IC.getImage().getScaledInstance(300, 300*iconHeight/iconWidth, Image.SCALE_DEFAULT));
//        viewImageBtn.setIcon(IC);
        JLabel imageLabel = new JLabel(IC);
        JLabel label = new JLabel(IC);
        JFrame frame = new JFrame("Image");
        frame.add(label);
        frame.pack();
        frame.setVisible(true);        
