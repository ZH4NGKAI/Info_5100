    private void uploadImageBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadImageBtnActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.setMultiSelectionEnabled(true);
          
        FileNameExtensionFilter filter = new FileNameExtensionFilter("jpg/jpeg/png/bmp", 
                "jpg", "png", "jpeg", "bmp");
        chooser.setFileFilter(filter);
        chooser.setAcceptAllFileFilterUsed(false);
        int returnVal = chooser.showOpenDialog(uploadImageBtn);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File[] arrfiles = chooser.getSelectedFiles();
            if (arrfiles == null || arrfiles.length == 0) {
                return;
            }
            FileInputStream input = null;
            FileOutputStream out = null;
            String path = "image/";
            try {
                for (File f : arrfiles) {
                    File dir = new File(path);
                    File[]fs = dir.listFiles();
                    HashSet<String> set = new HashSet<String>();
                    for (File file : fs) {
                        set.add(file.getName());
                    }
                    input = new FileInputStream(f);
                    byte[] buffer = new byte[1024];
                    String imageName = f.getName();
                    File des = new File(path, imageName);
                    out = new FileOutputStream(des);
                    int len = 0;
                    while (-1 != (len = input.read(buffer))) {
                        out.write(buffer, 0, len);
                    }
                    out.close();
                    input.close();
                    car.setImageName(imageName);
                }
                JOptionPane.showMessageDialog(null, "Upload image successfully!", "Information", 
                        JOptionPane.INFORMATION_MESSAGE);
            } catch (FileNotFoundException e1) {
                JOptionPane.showMessageDialog(null, "Fail to upload image！", "Error",
                        JOptionPane.ERROR_MESSAGE);
                e1.printStackTrace();
            } catch (IOException e1) {
                JOptionPane.showMessageDialog(null, "Fail to upload image！", "Error",
                        JOptionPane.ERROR_MESSAGE);
                e1.printStackTrace();
            }
        }
    }//GEN-LAST:event_uploadImageBtnActionPerformed


