    private void btnEvaFoodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEvaFoodActionPerformed
        // TODO add your handling code here:
        int evaluation = 0;
        if(btnF1.isSelected()) evaluation = 1;
        else if(btnF2.isSelected()) evaluation = 2;
        else if(btnF3.isSelected()) evaluation = 3;
        else if(btnF4.isSelected()) evaluation = 4;
        else if(btnF5.isSelected()) evaluation = 5;
        else {
            JOptionPane.showMessageDialog(null, "Please select a point for the food");
            return;
        }
        order.getRestaurant().setEvaluation(evaluation);
        for (Food food: order.getFoodDirectory().getFoodList()) {
            food.setEvaluate(evaluation);
        }
        JOptionPane.showMessageDialog(null, "Thank you for your evaluation!");
        btnEvaFood.setEnabled(false);
    }//GEN-LAST:event_btnEvaFoodActionPerformed

