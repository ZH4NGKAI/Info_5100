    private void btnEvaDSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEvaDSActionPerformed
        // TODO add your handling code here:
        int evaluation = 0;
        if(btnDS1.isSelected()) evaluation = 1;
        else if(btnDS2.isSelected()) evaluation = 2;
        else if(btnDS3.isSelected()) evaluation = 3;
        else if(btnDS4.isSelected()) evaluation = 4;
        else if(btnDS5.isSelected()) evaluation = 5;
        else {
            JOptionPane.showMessageDialog(null, "Please select a point for the food");
            return;
        }
        order.getDeliveryService().setEvaluation(evaluation);
        JOptionPane.showMessageDialog(null, "Thank you for your evaluation!");
        btnEvaDS.setEnabled(false);
    }//GEN-LAST:event_btnEvaDSActionPerformed

