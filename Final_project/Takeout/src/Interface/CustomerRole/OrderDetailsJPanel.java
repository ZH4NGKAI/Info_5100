    private void populate() {
        DefaultTableModel dtm = (DefaultTableModel)tblOrdered.getModel();
        dtm.setRowCount(0);
        float total = 0;
        for(Food food: order.getFoodDirectory().getFoodList()) {
            Object[] row = new Object[dtm.getColumnCount()];
             row[0] = food;
             row[1] = food.getPrice();
             total += food.getPrice();
             dtm.addRow(row);
        }
        txtTotal.setText(String.valueOf(total));
        txtRestaurant.setText(order.getRestaurant().getName());
        txtStatus.setText(order.getStatus());
        txtOrderTime.setText(order.getRequestDate());
        if (order.getStatus().equals("Delivered")) {
            btnAcknowledge.setEnabled(true);
            txtDeliveryTime.setText(order.getResolveDate());
        }
        
    }
    
