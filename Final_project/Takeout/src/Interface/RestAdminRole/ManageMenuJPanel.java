    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = menuTable.getSelectedRow();

        if(selectedRow>=0){
            Food a = (Food)menuTable.getValueAt(selectedRow, 0);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            userProcessContainer.add("UpdateAirplaneJPanel", 
                new UpdateFoodJPanel(userProcessContainer,enterprise, a));
            layout.next(userProcessContainer);

        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedrow = menuTable.getSelectedRow();
        if(selectedrow >= 0) {
            Food a = (Food)menuTable.getValueAt(selectedrow, 0);
            int reply = JOptionPane.showConfirmDialog(null, "Are you sure to delete?",
                    null,JOptionPane.YES_NO_OPTION);
            if(reply == 0) {
                enterprise.getFoodDirectory().getFoodList().remove(a);
            }else
            return;
            populateTable();
            JOptionPane.showMessageDialog(null, "Food has been deleted!");

        } else {
            JOptionPane.showMessageDialog(null, "Please select any row!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


