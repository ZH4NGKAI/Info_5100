    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) menuTable.getModel();
        
        model.setRowCount(0);
        
        for (Food food : enterprise.getFoodDirectory().getFoodList()){
            Object[] row = new Object[4];
            row[0] = food;
            row[1] = food.getPrice();
            row[2] = food.getEvaluate();
            row[3] = food.getSales();
            
            model.addRow(row);
        }
    }

