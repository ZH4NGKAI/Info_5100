    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)orderRequestTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[4];
            row[0] = (CookWorkRequest)request;
            row[1] = ((CookWorkRequest)request).getOrderWorkRequest().getFoodDirectory().getFoodList().size();
            row[2] = ((CookWorkRequest)request).getOrderWorkRequest().getTotal();
            row[3] = ((CookWorkRequest)request).getOrderWorkRequest().getStatus();
            
            model.addRow(row);
        }
    }

