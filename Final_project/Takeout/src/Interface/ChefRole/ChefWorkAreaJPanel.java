    private void btnFinishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinishActionPerformed
        // TODO add your handling code here:
        if(orderRequestTable.getSelectedRow()==-1)
        {
            JOptionPane.showMessageDialog(null, "Please select a request first!");
            return;
        }
        
        CookWorkRequest r = (CookWorkRequest)orderRequestTable.getValueAt(orderRequestTable.getSelectedRow(), 0);
        
        String status = r.getOrderWorkRequest().getStatus();
        
        //if order can be reject
        if(status.equals("cooking"))
        {
            r.getOrderWorkRequest().setStatus("Waiting For Delivery");
            populateTable();
            JOptionPane.showMessageDialog(null, "Request successfully finished!");
            return;
        }
        //if order can't be assigned
        else if(!status.equals("cooking"))
        {
            JOptionPane.showMessageDialog(null, "Request already finished!");
            return;
        }
    }//GEN-LAST:event_btnFinishActionPerformed


