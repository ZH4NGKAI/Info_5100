    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)deliveryRequestTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : enterprise.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[5];
            row[0] = (OrderWorkRequest)request;
            row[1] = ((OrderWorkRequest)request).getSender().getAddress();
            row[2] = ((OrderWorkRequest)request).getFoodDirectory().getFoodList().size();
            row[3] = ((OrderWorkRequest)request).getRestaurant().getAddress();
            row[4] = request.getStatus();
            
            model.addRow(row);
        }
    }
    
    public void popdeliComboBox()
    {
        deliComboBox.removeAllItems();
        
        
        for (Organization o: enterprise.getOrganizationDirectory().getOrganizationList())
        {
            if(o.getSupportedRole().get(0).getRoleType()==Role.RoleType.DeliveryGuy){
               
                deliComboBox.addItem(o);
            }
            
        }
        deliComboBox.setSelectedIndex(-1);
    }

