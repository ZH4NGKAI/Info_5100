    private void btnDetialsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetialsActionPerformed
        // TODO add your handling code here:
        int selectedRow = deliveryRequestTable.getSelectedRow();

        if(selectedRow>=0){
            OrderWorkRequest a = (OrderWorkRequest)deliveryRequestTable.getValueAt(selectedRow, 0);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            userProcessContainer.add("UpdateAirplaneJPanel", 
                new requestDetialJPanel(userProcessContainer,enterprise, a));
            layout.next(userProcessContainer);

        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_btnDetialsActionPerformed

    private void btnSentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSentActionPerformed
        // TODO add your handling code here:
        if(deliveryRequestTable.getSelectedRow()==-1)
        {
            JOptionPane.showMessageDialog(null, "Please select a request first!");
            return;
        }
        if(deliComboBox.getSelectedIndex() < 0) {
            JOptionPane.showMessageDialog(null, "Please select a delivery organization!");
            return;
        }
        OrderWorkRequest r = (OrderWorkRequest)deliveryRequestTable.getValueAt(deliveryRequestTable.getSelectedRow(), 0);
        
        String status = r.getStatus();
        
        if(status.equals("Waiting For Delivery")){
            DeliveryWorkRequest deliveryOrder = new DeliveryWorkRequest(r);
            DeliveryGuyOrganization deliveryOrg = (DeliveryGuyOrganization)deliComboBox.getSelectedItem();
            deliveryOrg.getWorkQueue().getWorkRequestList().add(deliveryOrder);
            deliveryOrder.getOrderWorkRequest().setStatus("Waiting For Pick");
            deliveryOrder.setSender(userAccount);
            populateTable();
            JOptionPane.showMessageDialog(null, "Request successfully sent!");
            return;
        }
        else if(!status.equals("Waiting For Delivery")){
            JOptionPane.showMessageDialog(null, "Request can't be sent!");
            return;
        }
        
           
       
        
    }//GEN-LAST:event_btnSentActionPerformed

