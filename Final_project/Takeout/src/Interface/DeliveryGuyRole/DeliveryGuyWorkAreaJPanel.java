    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)orderRequestTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : organization.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[5];
            row[0] = ((DeliveryWorkRequest)request).getOrderWorkRequest().getOrderId();
            row[1] = (DeliveryWorkRequest)request;
            row[2] = ((DeliveryWorkRequest)request).getOrderWorkRequest().getSender().getAddress();
            row[3] = ((DeliveryWorkRequest)request).getOrderWorkRequest().getRestaurant().getAddress();
//            row[3] = ((DeliveryWorkRequest)request).getOrderWorkRequest().getFoodDirectory().getFoodList().size();
            row[4] = ((DeliveryWorkRequest)request).getOrderWorkRequest().getStatus();
            
            model.addRow(row);
        }
        if(!userAccount.getWorkQueue().getWorkRequestList().isEmpty()) {
            btnAccept.setEnabled(false);
            btnFinish.setEnabled(true);
            txtOrderID.setText(String.valueOf(((DeliveryWorkRequest)userAccount.getWorkQueue()
                    .getWorkRequestList().get(0)).getOrderWorkRequest().getOrderId()));
        }
        else{
            btnAccept.setEnabled(true);
            btnFinish.setEnabled(false);
            txtOrderID.setText("No Order Delivering");
        }
    }

