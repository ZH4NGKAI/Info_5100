    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        // TODO add your handling code here:
        if(orderRequestTable.getSelectedRow()==-1)
        {
            JOptionPane.showMessageDialog(null, "Please select a request first!");
            return;
        }
        
        DeliveryWorkRequest r = (DeliveryWorkRequest)orderRequestTable.getValueAt(orderRequestTable.getSelectedRow(), 1);
        
        String status = r.getOrderWorkRequest().getStatus();
        
        if(!status.equals("Waiting For Pick"))
        {
            JOptionPane.showMessageDialog(null, "This order has been accepted by other delivery guys!");
            return;
        }
        userAccount.getWorkQueue().getWorkRequestList().add(r);
        r.getOrderWorkRequest().setStatus("Delivering");
        populateTable();
        JOptionPane.showMessageDialog(null, "Accept Order "+r.getOrderWorkRequest().getOrderId());
        //
       
    }//GEN-LAST:event_btnAcceptActionPerformed

    private void btnFinishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinishActionPerformed
        // TODO add your handling code here:
        DeliveryWorkRequest dwr = (DeliveryWorkRequest)userAccount.getWorkQueue().getWorkRequestList().get(0);
        dwr.getOrderWorkRequest().setStatus("Delivered");
        dwr.getOrderWorkRequest().setResolveDate();
        userAccount.getWorkQueue().getWorkRequestList().remove(dwr);
        populateTable();
    }//GEN-LAST:event_btnFinishActionPerformed


