    private void BackBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BackBtnActionPerformed
        // TODO add your handling code here:
        this.rightpanel.remove(this);
        Component[] componentArray = rightpanel.getComponents();
        //refresh table
        for(Component comp : componentArray){
            if(comp instanceof SysAdminWorkAreaJPanel){
                SysAdminWorkAreaJPanel manageP = (SysAdminWorkAreaJPanel) comp;
                manageP.populateTree();
            }
        }
        CardLayout cardlayout = (CardLayout)this.rightpanel.getLayout();
        cardlayout.previous(rightpanel);
    }//GEN-LAST:event_BackBtnActionPerformed


