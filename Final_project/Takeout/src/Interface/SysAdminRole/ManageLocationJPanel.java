    public void populate()
    {
        int rowCount = locationTbl.getRowCount();
        jScrollPane1.getViewport().setBackground(new Color(249,246,246));
        DefaultTableModel dtm = (DefaultTableModel)locationTbl.getModel();
        for(int i = rowCount - 1; i >= 0; i--)
        {
            dtm.removeRow(i);
        }
        
        for(Network nw : this.ecoSystem.getNetworkList()){
            Object row[] = new Object[dtm.getColumnCount()];
            row[0] = nw;                   
            dtm.addRow(row);
        }
    }

