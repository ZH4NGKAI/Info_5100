    private void RegisterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterBtnActionPerformed
        // TODO add your handling code here:
        String name = NameTxt.getText();
        Network network = (Network) LocationComboBox.getSelectedItem();
        if (name.isEmpty() || network == null)
        {
            JOptionPane.showMessageDialog(null, "Some input text is empty!");
            NameTxt.setText("");
            return;
        }
        if (!network.getEnterpriseDirectory().checkEnterpriseName(name)) {
            JOptionPane.showMessageDialog(null, "Existed Name!");
            NameTxt.setText("");
            return;
        }
        Enterprise enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(name, Enterprise.EnterpriseType.DeliveryService);
        JOptionPane.showMessageDialog(null, "Create Delivery Service Successfully!");
        NameTxt.setText("");
        populate();
           
    }//GEN-LAST:event_RegisterBtnActionPerformed

