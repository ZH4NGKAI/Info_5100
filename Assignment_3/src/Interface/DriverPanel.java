    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        try {
            Integer.parseInt(txtYear.getText());
        }
        catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please type in a integer number for Manufactured Year!");
            return;
        }
        
        try {
            Integer.parseInt(txtMaxSeats.getText());
        }
        catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please type in a integer number for Max Seats!");
            return;
        }
        
        try {
            Integer.parseInt(txtMinSeats.getText());
        }
        catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please type in a integer number for Min Seats!");
            return;
        }
        
        try {
            Integer.parseInt(txtSerialNum.getText());
        }
        catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please type in a integer number for Serial Number!");
            return;
        }
        
        Car car = carList.addCar();
        car.setBrand(txtBrand.getText());
        car.setAvailable(chkBoxAvai.isSelected());
        car.setAvailble_city(txtCity.getText());
        car.setMaintenance_certificate(chkBoxCert.isSelected());
        car.setManufactured_year(Integer.parseInt(txtYear.getText()));
        car.setMax_seats(Integer.parseInt(txtMaxSeats.getText()));
        car.setMin_seats(Integer.parseInt(txtMinSeats.getText()));
        car.setModel_num(txtModelNum.getText());
        car.setSerial_num(Integer.parseInt(txtSerialNum.getText()));
        JOptionPane.showMessageDialog(null, "Account Created Successfully");
        chkBoxAvai.setSelected(false);
        chkBoxCert.setSelected(false);
        txtCity.setText("");
        txtBrand.setText("");
        txtYear.setText("");
        txtMaxSeats.setText("");
        txtMinSeats.setText("");
        txtModelNum.setText("");
        txtSerialNum.setText("");
    }//GEN-LAST:event_btnCreateActionPerformed

